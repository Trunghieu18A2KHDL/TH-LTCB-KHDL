if diem_hoc_tap == 'A':
    print("sinh vien Xuat sac")
elif diem_hoc_tap == 'B':
    print("sinh vien Gioi")
elif diem_hoc_tap == 'C':
    print("sinh vien Kha")
elif diem_hoc_tap == 'D':
    print("sinh vien Trung binh")
elif diem_hoc_tap == 'E':
    print("sinh vien Yeu")
elif diem_hoc_tap == 'F':
    print("sinh vien Kem")
else:
    print("nhap sai diem")  